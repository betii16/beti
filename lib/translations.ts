// ── Helper pour récupérer une traduction ──────────────────────────
export function t(key: string, lang: Lang): string {
  const keys = key.split('.')
  let current: any = translations
  for (const k of keys) {
    current = current?.[k]
  }
  return current?.[lang] || current?.['fr'] || key
}
